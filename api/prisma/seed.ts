const prisma = new PrismaClient();
async function main() {
  try {
    // =========================
    // 🔥 OPTION 1: CLEAN RESET (PUT THIS FIRST)
    // =========================
    await prisma.option.deleteMany();
    await prisma.question.deleteMany();
    await prisma.match.deleteMany();
    // (optional but safer if needed)
    // await prisma.player.deleteMany();
    // await prisma.team.deleteMany();

    console.log('🧹 Old data cleared');
    await prisma.user.upsert({
      where: { email: 'admin@gmail.com' },
      update: {},
      create: {
        collegeId: 'admin1',
        email: 'admin@gmail.com',
        fullName: 'System Admin',
        password: await bcrypt.hash('admin123', 10),
        role: 'ADMIN',
      },
    });

    console.log('🔥 Admin seeded successfully');
    // =========================
    // 1. SEED TEAMS + PLAYERS
    // =========================
    for (const team of teamsData) {
      await prisma.team.upsert({
        where: { name: team.name },
        update: {},
        create: {
          name: team.name,
          players: {
            create: team.players.map((p) => ({
              name: p,
            })),
          },
        },
      });
    }

    console.log('✅ Teams seeded');

    // =========================
    // 2. SEED MATCHES + QUESTIONS
    // =========================
    for (const m of matchesData) {
      const teamA = await prisma.team.findUnique({
        where: { name: m.teamA },
        include: { players: true },
      });

      const teamB = await prisma.team.findUnique({
        where: { name: m.teamB },
        include: { players: true },
      });

      if (!teamA || !teamB) {
        console.log(`❌ Skipping match: ${m.teamA} vs ${m.teamB}`);
        continue;
      }

      const match = await prisma.match.create({
        data: {
          teamAId: teamA.id,
          teamBId: teamB.id,
          kickoffAt: new Date(m.kickoffAt),
          isLocked: false,
        },
      });

      // generate questions
      for (const [key, t] of Object.entries(QUESTION_TEMPLATES)) {
        const question = await prisma.question.create({
          data: {
            matchId: match.id,
            text: (t as any).text,
            type: (t as any).type,
            template: key,
          },
        });

        const options = (t as any).generateOptions?.({
          id: match.id,
          teamA,
          teamB,
        });

        if (options?.length) {
          await prisma.option.createMany({
            data: options.map((opt: any) => ({
              text: opt.text,
              isCorrect: opt.isCorrect || false,
              questionId: question.id,
            })),
          });
        }
      }
    }

    console.log('🔥 Seed completed successfully');
  } catch (err) {
    console.error('❌ Seed failed:', err);
  } finally {
    await prisma.$disconnect();
  }
}
main()
